/**
 * Topic: Ternary Operator
 * What we have done: Used the conditional operator (? :) as a shorthand if-else to find the maximum of two numbers.
 */
public class Practice6 {
    public static void main(String[] args) {
        System.out.println("--- Topic: Ternary Operator ---");
        int a = 10, b = 20;
        int max = (a > b) ? a : b;
        System.out.println("Max value is: " + max);
    }
}