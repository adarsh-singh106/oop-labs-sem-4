/**
 * Topic: Assignment Operators
 * What we have done: Demonstrated shorthand assignment operators (+= and *=) to update a variable's value.
 */
public class Practice5 {
    public static void main(String[] args) {
        System.out.println("--- Topic: Assignment Operators ---");
        int a = 10;
        a += 5; System.out.println("After a += 5: " + a);
        a *= 2; System.out.println("After a *= 2: " + a);
    }
}