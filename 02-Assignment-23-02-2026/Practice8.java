/**
 * Topic: Control Statements (if)
 * What we have done: Used a simple 'if' statement to execute code only when a specific condition (num > 10) is true.
 */
public class Practice8 {
    public static void main(String[] args) {
        System.out.println("--- Topic: if Statement ---");
        int num = 15;
        if (num > 10) {
            System.out.println(num + " is greater than 10");
        }
    }
}